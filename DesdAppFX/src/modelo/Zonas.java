package modelo;

public class Zonas {

//    Atributos
    private byte zonaId;
    private String nombre;
    private int muniId;

    //<editor-fold defaultstate="collapsed" desc="Metodos set y get">
    public byte getZonaId() {
        return zonaId;
    }

    public void setZonaId(byte zonaId) {
        this.zonaId = zonaId;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getMuniId() {
        return muniId;
    }

    public void setMuniId(int muniId) {
        this.muniId = muniId;
    }
//</editor-fold>
    @Override
    public String toString(){
        return nombre +" "+ muniId;
    }
}
